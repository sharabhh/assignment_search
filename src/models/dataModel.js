import mongoose from "mongoose";

const dataSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    phone: {
        type: Number,
        required: true,
        unique: true
    }
})
const dataModel = mongoose.model("dataModel", dataSchema) 

export default dataModel 